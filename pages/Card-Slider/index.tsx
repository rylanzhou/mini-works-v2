enum Status {
  AFTER = 'after',
  BEFORE = 'before',
  ACTIVE = 'active',
  READY = 'ready',
  READY_CANCEL = 'ready-cancel',
}

export default function CardSlider() {
  const [activeIndex, setActiveIndex] = useState(0);
  const [groupStatus, setGroupStatus] = useState<[Status, Status, Status]>([
    Status.ACTIVE,
    Status.AFTER,
    Status.AFTER,
  ]);

  const handlePrev = () => {
    // The reverse of handleNext
    const prevIndex = activeIndex - 1 >= 0 ? activeIndex - 1 : cardGroups.length - 1;

    const status: [Status, Status, Status] = [...groupStatus];

    // Current group's status becomes BEFORE
    status[activeIndex] = Status.BEFORE;
    // Prev group's status becomes READY from cancel button
    status[prevIndex] = Status.READY_CANCEL;
    setGroupStatus(status);
    // In a short delay, prev group's status becomes ACTIVE
    setTimeout(() => {
      status[prevIndex] = Status.ACTIVE;
      setActiveIndex(prevIndex);
    }, 0);
  };

  const handleNext = () => {
    // Get next active group index
    const nextIndex = activeIndex + 1 < cardGroups.length ? activeIndex + 1 : 0;

    const status: [Status, Status, Status] = [...groupStatus];

    // Current group's status becomes AFTER
    status[activeIndex] = Status.AFTER;
    // Next group's status becomes READY
    status[nextIndex] = Status.READY;
    setGroupStatus(status);
    // In a short delay, next group's status becomes ACTIVE
    setTimeout(() => {
      status[nextIndex] = Status.ACTIVE;
      setActiveIndex(nextIndex);
    }, 0);
  };

  return (
    <div className="body" style={{ backgroundColor: '#212121', flexDirection: 'column' }}>
      <div className={styles['card-groups']}>
        {cardGroups.map((urls, index) => (
          <div key={index} className={styles['card-group']} data-status={groupStatus[index]}>
            {urls.map((url, index) => (
              <div
                key={index}
                className={`${styles.card} ${index & 1 ? styles.big : styles.small}`}
                style={{ backgroundImage: `url(${url})` }}
              ></div>
            ))}
          </div>
        ))}
      </div>

      <div className={styles.buttons}>
        <IoCloseOutline onClick={() => handlePrev()} />
        <IoHeart className={styles.like} onClick={() => handleNext()} />
      </div>
    </div>
  );
}
